            <p>(1) Salvor Integration into the Response Organization</p>
            <p>The size and complexity of a salvage operation will dictate the configuration of the Incident Command
                organization formed under the Unified Command</p>

            <p>Salvage response can range from simple to complex. Four variables to be considered in organizing for a
                salvage response are:</p>

            <ul>
                <li>Size of the incident</li>
                <li>Complexity of the incident response, and</li>
                <li>Relative significance of salvage operations with respect to other aspects of the response</li>
                <li>Public firefighting resource coordination</li>
            </ul>

            <p>This section is intended to address the coordination between the differing response organizations <b>before</b>
                an incident occurs. It entails inter-organizational outreach, participation in the ACP process, and
                communication between the planholders, resource providers, and other affected port partners. Donjon-SMIT
                considers it important to ensure that all the pre-incident coordination is in place prior to an
                emergency situation. <b>We acknowledge, however, that the Incident Commander/Unified Command will be
                    responsible for coordination activities after an incident occurs and during all phases of the
                    incident response.</b></p>

            <p>Coordination with other resources, organizations and OSROs includes pre-incident inter-organizational
                outreach, and communication between the planholders, resource providers, and other affected port
                partners. Participation in the ACP process is an important part of this planning. Pre-incident
                coordination with firefighting resources that are public assets is completed through local maritime
                firefighting groups and individual coordination with each of the municipal departments.</p>

            <p><b>How Donjon-SMIT assessment personnel coordinate response activity with oil spill removal
                    organizations.</b></p>
            <p>Salvage and firefighting integration into the ICS is an important element of any response, particularly
                when these elements of the response are predominant or are critical. Since the Salvage Master or his
                representative is often the most knowledgeable person in the response organization, the access of that
                person to the Incident Command/Unified Command is important. Coordination and response organization
                dynamics are always changing, but utilizing the unified command structure with salvage and firefighting
                efforts appropriately interjected at a high enough level to ensure that dissemination of information
                required for correct decision-making will ensure success.</p>

            <p>Salvage Masters and their command center representatives have the most intimate knowledge of a vessel
                casualty. Historically, the salvors and marine firefighters have been placed in the Operations Section,
                however room must be made, at the prerogative of the Incident Commander/Unified Command, to structure
                the ICS organization to best fit the incident's needs. At the onset of an incident, Donjon-SMIT will
                rapidly interface with the Operations Section Chief and the Unified Command to ensure the correct
                placement of the salvage team within the ICS organization.</p>

            <p>Scenarios 1 through 4 below, illustrated by Figures 1 through 4, are examples of the organization of
                salvage response efforts within an ICS structure. The Scenario 1 organization depicted in Figure 1 is
                for a response that involves only limited salvage operations while Scenario 3, illustrated by Figure 3,
                is an example of organizing for an extremely complex salvage incident. Scenario 4 shows specific public
                firefighting resource coordination.</p>

            <p>(1) <u><b>Scenario 1:</b></u> Vessel casualties that require only limited salvage operations before the
                vessel can be delivered safely may be managed successfully from within the standard ICS structure, with
                the Operations Section Chief establishing a Salvage Group and placing a experienced salvor in Operations
                as a technical specialist.</p>

