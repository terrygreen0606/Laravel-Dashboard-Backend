            <p>(4) <u><b>Scenario 4:</b></u> Coordination and Response with Public Fire Departments</p>
            <p>Section 8000 of the Area Contingency Plans Base Plan, &ldquo;Marine Firefighting Executive Summary&rdquo;
                states:</p>
            <p>&ldquo;8120 Regional and Local Policy</p>
            <p>Whether there is a vessel fire or shore side fire at a waterfront facility, or if there is a fire aboard
                a vessel that is underway anywhere within the COTP's &hellip; Area of Responsibility (AOR), primary
                responsibility for firefighting lies with the municipality in whose jurisdiction the facility/vessel
                lies. The municipal fire department in whose jurisdiction the fire lies will be the Incident Commander
                (IC) for the firefighting activities. The IC is responsible for the coordination of all fire fighting
                activities. Assistance may, and in most cases will, be provided by other municipal fire departments via
                mutual aid agreements.&rdquo;</p>
            <p>Clearly, as part of the ACP, the local fire department is (properly) expected to be the Incident
                Commander and to decide when/if and how any private resources are integrated into the response
                organization. As a private response provider and holder of the vessel pre-fire plans, Donjon-SMIT meets
                with and discusses procedures with the municipal firefighting organization and potential Incident
                Commander for the locations covered under this plan.</p>
            <p>It is always Donjon-SMIT's policy to defer to the local municipality as IC, and Donjon-SMIT will assume
                responsibility (in terms of IC) only with local IC/fire department concurrence. The best value-added
                assistance from Donjon-SMIT will be the sharing of marine firefighting expertise and specifics and the
                provision of specialized marine firefighting equipment (such as inert gas generators).</p>
            <p>While each municipality is a little different, they all follow the tenets of the National Incident
                Management System and that is the crux of our coordination. We will operate wholly within that system as
                well.</p>
            <p>Similarly, any firefighting providers will be working for/with Donjon-SMIT and as such will be directly
                integrated into any local UCS/ICS. They will NOT be operating independently.</p>
            <p>Coordination between the differing response organizations and Donjon-SMIT includes pre-incident planning
                that entails inter-organizational outreach, participation in the ACP process, and communication between
                the planholders, resource providers, and other affected port partners. It is vital to ensure that all
                the pre-incident coordination is in place prior to an emergency situation.</p>
            <p>Finally, for each municipality for which we are relying on initial support, we will share pre-fire plans
                so that they can be reviewed and agreed upon prior to an incident.</p>
            <p class="list-section">c. <span>(2)</span> Firefighting Integration into the Response Organization</p>
            <p>The size and complexity of a firefighting operation will dictate the configuration of the Incident
                Command organization formed under the Unified Command.</p>

            <p>Typical responsibilities of the first fire responder on-scene include:</p>

            <ul>
                <li>Assume position as the lead in the Unified Command for marine firefighting operations if the vessel
                    on fire is moored to a shore side facility or if the vessel is underway when the fire occurs and the
                    master/crew cannot extinguish the fire without assistance.
                </li>
                <li>Establish and staff the Unified Command Post.</li>
                <li>Dispatch necessary personnel and equipment.</li>
                <li>Determine the need for and request mutual aid, such as on-scene initial assessment, equipment
                    support, fireboats and appropriate medical aid.
                </li>
                <li>If working with a municipal fire department, support the On-Scene Incident Commander (generally the
                    local fire chief) and provide maritime firefighting expertise and advice. Generally Donjon-SMIT will
                    assume control of the incident only when relinquished by the local fire chief. DJS will work within
                    the Unified Command to coordinate all other aspects of the response, including waterside security,
                    landside traffic and crowd control, scene security, and evacuation.
                </li>
                <li>Provide portable communications equipment to response personnel from outside agencies.</li>
            </ul>

            <p>Variables to be considered in organizing for a Firefighting response are: </p>
            <ul>
                <li>Size of the incident</li>
                <li>Complexity of the incident response, and</li>
                <li>Relative significance of firefighting operations with respect to other aspects of the response</li>
            </ul>
